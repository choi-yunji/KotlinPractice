//fun = function의 약자, 함수
//main = 함수의 시작과 끝을 나타내며 {} 중괄호 사이에 main 함수에 담을 명령어 작성
fun main(args: Array<String>):Unit{
    println("Hello,Kotlin!")
}

// JAVA와 KOTLIN 차이
// 함수가 반드시 클래스에 묶여 있어야 하는 자바와는 달리 코틀린은 클래스에서 완전히 독립된 함수를 가질 수 있다
// 이를 패키지 레벨 함수라 한다.
// 코틀린은 변수의 타입을 맨 끝에 적는다 args: Array<String> 부분은 자바의 String [] args 와 같다.
// 코틀린은 세미콜론(;)을 적지 않는다. 적는다고 해서 오류가 나지는 않으나 세미콜론은 두문장을 한줄에 작성할 때 사용한다.


